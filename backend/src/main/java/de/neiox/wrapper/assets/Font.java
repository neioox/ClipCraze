package de.neiox.wrapper.assets;

public class Font {

    public String name = "";
    public int size = 0;
    public String  color = "";
    public String fontPath = "";

    public Font(String name, int size, String color, String fontPath) {
        this.name = name;
        this.size = size;
        this.color = color;
        this.fontPath = fontPath;
    }
}
