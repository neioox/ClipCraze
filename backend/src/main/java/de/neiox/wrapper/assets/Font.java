package de.neiox.wrapper.assets;

public class Font {

    public String name = "";

    public int size = 0;


    public String  color = "";
    public int alignment = 0;


    public Font(String name, int size, String color, int alignment) {
        this.name = name;
        this.size = size;
        this.color = color;
        this.alignment = alignment;
    }

}
